class NúmerosDecimales {
  public static void main(String[] args) {
    // **********************************
    // Arregla la siguientes conversiones.
    // **********************************
    byte a = 50;
    byte b = 50;
    byte resultadoAB = a + b; // Solo puedes cambiar esta línea.
    System.out.println("La suma de a y b es: " + resultadoAB);

    float resultadoFloat = 20.0 + 70.0; // Solo puedes cambiar esta línea.
    System.out.println("La suma de resultadoFloat es: " + resultadoFloat);

    double c = 10.4;
    int d = 10;
    int resultadoCDEntero = c + d; // Solo puedes cambiar esta línea.
    double resultadoCDDouble = c + d;
    System.out.println("La suma de resultadoCDEntero es: " + resultadoCDEntero);
    System.out.println("La suma de resultadoCDDouble es: " + resultadoCDDouble);
  }
}
